    def __init__(self):
        self.cust_id = int(input("Enter the Customer Id: "))
        self.cust_name = input("Enter Customer Name: ")
        self.cust_address = input("Enter Customer Address: ")
        self.cust_phone = input("Enter Customer Phone Number")
        self.cust_email = input("Enter Customer Email")
        self.street = input("Enter Street Name")
        self.city = input("Enter City Name")
        self.state = input("Enter State Name")
        self.country = input("Enter Country Name")



